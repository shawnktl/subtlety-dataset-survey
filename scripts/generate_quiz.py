#!/usr/bin/env python3
"""Generate quiz questions targeting low-confidence and overdue topics.

Usage:
    python3 scripts/generate_quiz.py          # 10 questions to stdout
    python3 scripts/generate_quiz.py -n 20    # 20 questions
    python3 scripts/generate_quiz.py -o quiz.md  # Write to file
"""

import argparse
import os
import random
from datetime import date, timedelta

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
TOPICS_PATH = os.path.join(REPO_ROOT, "study", "topics.md")

INTERVALS = {1: 1, 2: 3, 3: 7, 4: 14, 5: 30}

QUESTION_TYPES = [
    "Recall — Define or describe this topic",
    "Recognition — Identify key features or characteristics",
    "Application — Apply knowledge to a scenario",
    "Comparison — Compare/contrast with a related topic",
    "Clinical/Practical — How does this apply in practice?",
]


def parse_topics(path):
    """Parse topic table. Returns list of dicts."""
    topics = []
    with open(path, "r", encoding="utf-8") as f:
        lines = f.readlines()

    header_found = False
    for line in lines:
        line = line.strip()
        if line.startswith("| Topic"):
            header_found = True
            continue
        if line.startswith("|---") or not header_found or not line.startswith("|"):
            continue
        if line.startswith("**") or line.startswith("<!--"):
            continue

        cells = [c.strip() for c in line.split("|")[1:-1]]
        if len(cells) < 3 or not cells[0]:
            continue

        topic = cells[0]
        category = cells[1]
        try:
            confidence = int(cells[2])
        except ValueError:
            confidence = 1

        last_reviewed = cells[3] if len(cells) > 3 else "—"

        topics.append({
            "topic": topic,
            "category": category,
            "confidence": confidence,
            "last_reviewed": last_reviewed,
        })

    return topics


def calculate_priority(topic):
    """Higher priority = lower confidence + more overdue. Returns float."""
    conf = topic["confidence"]
    # Base priority from confidence (inverted: lower confidence = higher priority)
    priority = (6 - conf) * 10  # 50 for conf 1, 10 for conf 5

    # Bonus for overdue
    lr = topic["last_reviewed"]
    if lr == "—" or not lr:
        priority += 20  # Never reviewed = high priority
    else:
        try:
            last_date = date.fromisoformat(lr)
            interval = INTERVALS.get(conf, 7)
            due_date = last_date + timedelta(days=interval)
            days_overdue = (date.today() - due_date).days
            if days_overdue > 0:
                priority += min(days_overdue * 2, 30)
        except ValueError:
            priority += 10

    return priority


def main():
    parser = argparse.ArgumentParser(description="Generate quiz questions")
    parser.add_argument("-n", type=int, default=10, help="Number of questions")
    parser.add_argument("-o", type=str, help="Output file (default: stdout)")
    args = parser.parse_args()

    if not os.path.isfile(TOPICS_PATH):
        print(f"Error: {TOPICS_PATH} not found")
        return

    topics = parse_topics(TOPICS_PATH)
    if not topics:
        print("No topics found")
        return

    # Weight by priority
    for t in topics:
        t["_priority"] = calculate_priority(t)

    # Sort by priority (highest first) and take top N
    topics.sort(key=lambda t: t["_priority"], reverse=True)
    selected = topics[:args.n]

    # Shuffle so the quiz isn't always in priority order
    random.shuffle(selected)

    today = date.today()
    output = f"# Quiz — {today.isoformat()}\n\n"
    output += f"*{len(selected)} questions targeting low-confidence and overdue topics.*\n"
    output += f"*Generated by generate_quiz.py. Enhance with an agent for richer questions.*\n\n"
    output += "---\n\n"

    for i, t in enumerate(selected, 1):
        qtype = random.choice(QUESTION_TYPES)
        output += f"### Q{i}. {t['topic']}\n\n"
        output += f"**Category:** {t['category']} | **Confidence:** {t['confidence']}/5\n\n"
        output += f"**Question type:** {qtype}\n\n"
        output += f"**Your answer:**\n\n\n\n"
        output += "---\n\n"

    output += f"## After the Quiz\n\n"
    output += f"For each question, rate your answer:\n"
    output += f"- Got it wrong or blank → decrease confidence by 1\n"
    output += f"- Got it with effort → keep same\n"
    output += f"- Got it easily → increase confidence by 1\n\n"
    output += f"Then update study/topics.md and run `python3 scripts/update_schedule.py`\n"

    if args.o:
        with open(args.o, "w", encoding="utf-8") as f:
            f.write(output)
        print(f"Quiz written to {args.o}")
    else:
        print(output)


if __name__ == "__main__":
    main()
